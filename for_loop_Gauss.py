# Gauss's summation to 100 using for loop!

total = 0
for num in range(101):
    total = total + num
print(total)

# decrements starting and 5 to -1 (non-inclusive)
for i in range(5, -1, -1):
    print(i)

print('___________________________')

# increments by 2 from 0 to 11 (non-inclusive)
for i in range(0, 11, 2):
    print(i)
